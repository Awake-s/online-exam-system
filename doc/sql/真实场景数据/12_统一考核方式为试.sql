-- ============================================================================
-- 12_统一考核方式为试.sql
--
-- 背景:
--   本系统是「在线考试系统」(OES), 题库设计为 5 种题型:
--     单选 / 多选 / 判断 / 填空 / 简答
--   全部为"考试课"语义 (闭卷笔试形式), 无法承载"考查课"的核心评价物
--   (论文 / 作品 / 答辩 / 实操等), 故课程级别的 "试/查" 区分对当前系统
--   行为零影响 — 选「考查」也只能走与「考试」相同的客观+简答出题流程.
--
--   主流 OES SaaS (超星 / 智慧树 / 中国大学 MOOC) 的标准做法:
--     不在课程级区分 "考试/考查", 而是在活动级区分 "作业/测验/考试".
--   该设计已记录在 ROADMAP_考核演进.md, 列入未来迭代路线.
--
--   现阶段决策: 数据层与表现层全面收敛为「考试」单一形态, 让产品定位回归
--   纯净的"在线笔试"系统; exam_type 字段保留作为未来扩展余地.
-- ============================================================================

USE online_exam_system;

START TRANSACTION;

-- 把所有 "查" 统一为 "试" (412 条 → 全部并入 1091 条考试课)
UPDATE edu_subject SET exam_type = '试' WHERE exam_type = '查';

COMMIT;

-- ============================================================================
-- 验证
-- ============================================================================

-- 期望: 仅剩 '试' 一类, 数量 = 总记录数
SELECT exam_type AS '考核', COUNT(*) AS '数量'
  FROM edu_subject
 WHERE grade IS NOT NULL
 GROUP BY exam_type;

SELECT COUNT(*) AS '科目总数' FROM edu_subject;
