# v8 描述统一: 6 字段 description 拼装
# 格式: [课程类型] · [专业简称][年级]第[N]学期 · [credit]学分/[hours]学时 · [核心内容]
# 示例: 专业核心必修 · 计科24级第5学期 · 4学分/72学时 · SSM/Spring 框架
# 理由:
#   1. 与 06 升级 SQL 中 14 条计科 24 级 description 完全同构, 全系统一致
#   2. course_type/credit/semester 三列已被 v7 KISS 物理删除, 信息只能落 description
#   3. 答辩演示时单字段即可读完培养方案核心信息, 无需多列展开
# ============================================================
def _normalize_ctype(ctype):
    """补全'必修'后缀: 学科基础/专业核心 默认必修, 选修保持原样"""
    if "必修" in ctype or "选修" in ctype:
        return ctype
    return ctype + "必修"


def _extract_content(desc):
    """去除元组 desc 中的分类前缀, 仅保留核心内容
    如 '专业核心·SSM/Spring 框架' -> 'SSM/Spring 框架'
    """
    if "·" in desc:
        return desc.split("·", 1)[1].strip()
    return desc.strip()


def build_description(ctype, short, grade, semester, credit, hours, desc):
    """拼装 6 字段标准 description"""
    ctype_full = _normalize_ctype(ctype)
    grade_yy = str(grade)[-2:]               # 2022 -> "22"
    credit_str = f"{credit:g}"               # 3.0 -> "3", 0.5 -> "0.5"
    content = _extract_content(desc)
    return (
        f"{ctype_full} · {short}{grade_yy}级第{semester}学期 · "
        f"{credit_str}学分/{hours}学时 · {content}"
    )


