def get_common_courses(major_id, grade):
    """根据专业类别和年级返回应修读的全部通识必修课"""
    courses = list(COMMON_ALL)  # 全校通识 12 条
    if grade == 2024:
        courses += COMMON_2024_ONLY  # 24 级新增思政
    if major_id not in LANGUAGES:
        courses += COMMON_ENGLISH    # 非外语类修大学英语
    # 按专业类别附加学科基础
    if major_id in ENGINEERING:
        courses += ENG_BASE
    elif major_id in MANAGEMENT:
        courses += MGT_BASE
    elif major_id in HUMANITIES:
        courses += HUM_BASE
    elif major_id in LANGUAGES:
        courses += LANG_BASE
    elif major_id in ARTS:
        courses += ART_BASE
    return courses


def get_all_courses(major_id, grade):
    """返回该专业该年级应开设的全部"可考试"课程列表

    本系统是在线考试系统(OES), 学科表只承载"可以承载题库"的科目.
    以下 4 类不可考课程会被自动过滤:
      1. 集中实践类 (毕业设计/课程实践/综合实习): 答辩/作品评分
      2. 大学体育(一)~(四): 体测+技能考核
      3. 思想政治理论课综合实践(*): 实践报告+论文
      4. 物理实验B: 实验报告评分
    """
    courses = get_common_courses(major_id, grade)
    if major_id == 100:  # 计算机科学与技术
        courses += CS_CORE_FULL + CS_ELECTIVES + CS_PRACTICES
    elif major_id in MAJOR_TEMPLATES:
        courses += MAJOR_TEMPLATES[major_id]
    return [c for c in courses if _is_examable(c)]


# OES 不可考课程黑名单 (前缀匹配)
_NON_EXAMABLE_NAME_PREFIXES = ('大学体育(', '思想政治理论课综合实践')
_NON_EXAMABLE_NAMES = {'物理实验B'}


def _is_examable(course_tuple):
    """判断一门课程是否适合上线考试 (可承载题库)"""
    name, ctype = course_tuple[0], course_tuple[1]
    if ctype == '集中实践':
        return False
    if name in _NON_EXAMABLE_NAMES:
        return False
    if any(name.startswith(p) for p in _NON_EXAMABLE_NAME_PREFIXES):
        return False
    return True


def escape_sql(s):
    """简单 SQL 转义"""
    return s.replace("\\", "\\\\").replace("'", "\\'")


def build_full_subject_name(course_name, grade, major_short):
    """学科名直接使用课程名;年级与专业由 grade/major_id 字段承担区分职责"""
    return course_name


