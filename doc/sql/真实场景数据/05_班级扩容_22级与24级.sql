-- 三、验证断言
-- ============================================================
SELECT '总班级数'           AS 项目, COUNT(*) AS 实际, 75 AS 预期 FROM edu_class WHERE id >= 100
UNION ALL SELECT '22级班级数',   COUNT(*), 33 FROM edu_class WHERE grade = '2022级'
UNION ALL SELECT '24级班级数',   COUNT(*), 42 FROM edu_class WHERE grade = '2024级'
UNION ALL SELECT '旧24级班级(已绑定学生)',  COUNT(*), 14 FROM edu_class WHERE id BETWEEN 100 AND 113
UNION ALL SELECT '新增22级班级', COUNT(*), 33 FROM edu_class WHERE id BETWEEN 200 AND 232
UNION ALL SELECT '新增24级班级', COUNT(*), 28 FROM edu_class WHERE id BETWEEN 300 AND 327;

-- 按年级 + 专业统计班级数
SELECT
  c.grade AS 年级,
  m.id    AS 专业ID,
  m.major_name AS 专业,
  COUNT(c.id) AS 班级数
FROM edu_class c
LEFT JOIN edu_major m ON m.id = c.major_id
WHERE c.id >= 100
GROUP BY c.grade, m.id, m.major_name
ORDER BY c.grade, m.id;

